fn load_golden(name: &str) -> Value {
    let path = format!("{}/tests/fixtures/{}", env!("CARGO_MANIFEST_DIR"), name);
    let text = std::fs::read_to_string(&path).unwrap_or_else(|e| panic!("read {path}: {e}"));
    serde_json::from_str(&text).unwrap_or_else(|e| panic!("parse {path}: {e}"))
}

fn as_i32_vec(v: &Value) -> Vec<i32> {
    v.as_array()
        .expect("json array")
        .iter()
        .map(|x| x.as_i64().expect("int") as i32)
        .collect()
}

fn as_f64_vec(v: &Value) -> Vec<f64> {
    v.as_array()
        .expect("json array")
        .iter()
        .map(|x| x.as_f64().expect("float"))
        .collect()
}

// -------------------------------------------------------------------------------------------------
// Stage 1 — tokenizers → exact token ids (integer-exact; CPU/scalar).
// -------------------------------------------------------------------------------------------------

#[test]
fn stage1_tokenizers_match_golden_ids() {
    let g = load_golden("tokenizer_golden.json");
    let cases = g["cases"].as_array().expect("cases array");
    // Hard-assert element count first — a loop over an empty array would assert nothing.
    assert_eq!(cases.len(), 6, "expected 6 fixture-prompt cases");

    let tk = AnimaTokenizers::load().expect("load vendored tokenizers");
    for case in cases {
        let prompt = case["prompt"].as_str().expect("prompt");
        let want_qwen = as_i32_vec(&case["qwen_ids"]);
        let want_qwen_mask = as_i32_vec(&case["qwen_mask"]);
        let want_t5 = as_i32_vec(&case["t5_ids"]);

        let (qids, qmask) = tk.encode_qwen(prompt).expect("encode_qwen");
        let got_qwen = qids.as_slice::<i32>().to_vec();
        let got_qmask = qmask.as_slice::<i32>().to_vec();
        let t5 = tk.encode_t5(prompt).expect("encode_t5");
        let got_t5 = t5.as_slice::<i32>().to_vec();

        assert_eq!(
            got_qwen, want_qwen,
            "Qwen ids mismatch for prompt {prompt:?}"
        );
        assert_eq!(
            got_qmask, want_qwen_mask,
            "Qwen mask mismatch for prompt {prompt:?}"
        );
        assert_eq!(got_t5, want_t5, "T5 ids mismatch for prompt {prompt:?}");
    }

    // The empty prompt is the load-bearing edge (reference: Qwen [[0]]/mask[[0]], T5 [[1]] EOS).
    let empty = cases
        .iter()
        .find(|c| c["prompt"].as_str() == Some(""))
        .expect("empty-prompt case present");
    assert_eq!(as_i32_vec(&empty["qwen_ids"]), vec![0]);
    assert_eq!(as_i32_vec(&empty["qwen_mask"]), vec![0]);
    assert_eq!(as_i32_vec(&empty["t5_ids"]), vec![1]);
}

// -------------------------------------------------------------------------------------------------
// Stage 5 — sigma schedule + timestep convention (~1e-6; CPU/scalar).
// -------------------------------------------------------------------------------------------------

#[test]
fn stage5_sigma_schedule_matches_golden() {
    let g = load_golden("sigma_schedule_golden.json");
    let schedules = g["schedules"].as_object().expect("schedules object");
    assert_eq!(schedules.len(), 4, "expected schedules for N = 1,10,30,50");

    for n in [1usize, 10, 30, 50] {
        let entry = &schedules[&n.to_string()];
        let want_sigmas = as_f64_vec(&entry["sigmas"]);
        let want_timesteps = as_f64_vec(&entry["timesteps"]);
        // Reference appends a terminal 0.0 → N+1 sigmas; N timesteps (pre-terminal).
        assert_eq!(want_sigmas.len(), n + 1, "N={n}: sigma count");
        assert_eq!(want_timesteps.len(), n, "N={n}: timestep count");

        let got = anima_sigmas(n);
        assert_eq!(got.len(), n + 1, "N={n}: anima_sigmas length");
        for (i, (&a, &b)) in got.iter().zip(&want_sigmas).enumerate() {
            assert!(
                (a as f64 - b).abs() < 1e-6,
                "N={n} sigma[{i}]: got {a}, want {b}"
            );
        }
        // The DiT consumes the RAW σ as its timestep; the diffusers scheduler stores σ·1000 and divides
        // it back before the DiT (denoise.py:47). Assert BOTH: (a) golden self-consistency
        // timesteps[i] == sigmas[i]·1000, and (b) our σ·1000 reproduces the golden timesteps.
        for i in 0..n {
            assert!(
                (want_timesteps[i] - want_sigmas[i] * 1000.0).abs() < 1e-2,
                "N={n} golden timestep[{i}] != sigma·1000"
            );
            assert!(
                (got[i] as f64 * 1000.0 - want_timesteps[i]).abs() < 1e-2,
                "N={n} σ·1000[{i}]: got {}, want {}",
                got[i] as f64 * 1000.0,
                want_timesteps[i]
            );
        }
    }

    // N=1 is the degenerate single-step case (shift(1.0)=1.0).
    assert_eq!(anima_sigmas(1), vec![1.0, 0.0]);
}

