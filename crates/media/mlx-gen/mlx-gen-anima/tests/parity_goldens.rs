/// A tiny Cosmos DiT config with the SAME structure as [`DitConfig::anima`] (17-ch concat, 3-axis NTK
/// RoPE, adaLN-LoRA, patch `(1,2,2)`) but small dims, so the forward is cheap and the golden is tiny.
fn synth_cfg() -> DitConfig {
    DitConfig {
        in_channels: 16, // → 17 after the padding-mask concat (the 17-vs-16 regression surface)
        out_channels: 16,
        num_attention_heads: 2,
        attention_head_dim: 6, // hidden = 12; head_dim 6 splits cleanly for the RoPE (dim_t/h/w = 2/2/2)
        num_layers: 2,
        mlp_ratio: 4.0,
        text_embed_dim: 8, // cross-attn context (encoder) dim
        adaln_lora_dim: 8,
        max_size: (128, 120, 120),
        patch_size: (1, 2, 2),
        rope_scale: (1.0, 4.0, 4.0),
        concat_padding_mask: true,
    }
}

/// FNV-1a of the key → a stable, order-independent, portable per-tensor seed.
fn key_seed(key: &str) -> u64 {
    let mut h = 0xcbf2_9ce4_8422_2325u64;
    for b in key.as_bytes() {
        h ^= *b as u64;
        h = h.wrapping_mul(0x0000_0100_0000_01b3);
    }
    h
}

/// Deterministic LCG in `[-1, 1)` — the same integer recurrence as the real-weights tests' `lcg_fill`,
/// so weights reproduce bit-for-bit across machines and runs.
fn synth_lcg(n: usize, seed: u64) -> Vec<f32> {
    let mut s = seed & 0x7fff_ffff;
    (0..n)
        .map(|_| {
            s = (s.wrapping_mul(1103515245).wrapping_add(12345)) & 0x7fff_ffff;
            (s as f64 / 2147483647.0 * 2.0 - 1.0) as f32
        })
        .collect()
}

/// Insert a synthetic tensor for `key`. Norm/scale weights (`*norm.weight`) init near 1.0; linear
/// weights init at `0.3·lcg` — large enough that adaLN scale/shift/gate are O(1) (so a modulation-wiring
/// regression actually moves the output), yet small enough that the LayerNorm-gated two-layer forward
/// stays finite and well-behaved (verified: no NaN; std ~O(0.1–1)).
fn synth_tensor(w: &mut Weights, key: &str, shape: &[i32]) {
    let n: usize = shape.iter().map(|&d| d as usize).product();
    let raw = synth_lcg(n, key_seed(key));
    let data: Vec<f32> = if key.ends_with("norm.weight") {
        raw.iter().map(|&v| 1.0 + 0.1 * v).collect()
    } else {
        raw.iter().map(|&v| 0.3 * v).collect()
    };
    w.insert(key, Array::from_slice(&data, shape));
}

/// Build the full dense-f32 weight set the [`CosmosDiT`] loader requires (prefix `"net"`, Cosmos-original
/// key names). Every shape is derived from `cfg`, so this stays in lockstep with the loader.
fn synth_weights(cfg: &DitConfig) -> Weights {
    let h = cfg.hidden_size() as i32; // 12
    let hd = cfg.attention_head_dim as i32; // 6
    let lora = cfg.adaln_lora_dim as i32; // 8
    let ctx = cfg.text_embed_dim as i32; // 8 (cross-attn kv input)
    let ff = (cfg.mlp_ratio * cfg.hidden_size() as f32) as i32; // 48
    let (pt, ph, pw) = cfg.patch_size;
    let (pt, ph, pw) = (pt as i32, ph as i32, pw as i32);
    let patch_in = cfg.patch_in_channels() as i32 * pt * ph * pw; // 17·1·2·2 = 68
    let proj_out = ph * pw * pt * cfg.out_channels as i32; // 2·2·1·16 = 64
    let mut w = Weights::empty();
    synth_tensor(&mut w, "net.x_embedder.proj.1.weight", &[h, patch_in]);
    synth_tensor(&mut w, "net.t_embedder.1.linear_1.weight", &[3 * h, h]);
    synth_tensor(&mut w, "net.t_embedder.1.linear_2.weight", &[3 * h, 3 * h]);
    synth_tensor(&mut w, "net.t_embedding_norm.weight", &[h]);
    for i in 0..cfg.num_layers {
        let b = format!("net.blocks.{i}");
        for m in [
            "adaln_modulation_self_attn",
            "adaln_modulation_cross_attn",
            "adaln_modulation_mlp",
        ] {
            synth_tensor(&mut w, &format!("{b}.{m}.1.weight"), &[lora, h]);
            synth_tensor(&mut w, &format!("{b}.{m}.2.weight"), &[3 * h, lora]);
        }
        for (attn, kv_in) in [("self_attn", h), ("cross_attn", ctx)] {
            synth_tensor(&mut w, &format!("{b}.{attn}.q_proj.weight"), &[h, h]);
            synth_tensor(&mut w, &format!("{b}.{attn}.k_proj.weight"), &[h, kv_in]);
            synth_tensor(&mut w, &format!("{b}.{attn}.v_proj.weight"), &[h, kv_in]);
            synth_tensor(&mut w, &format!("{b}.{attn}.output_proj.weight"), &[h, h]);
            synth_tensor(&mut w, &format!("{b}.{attn}.q_norm.weight"), &[hd]);
            synth_tensor(&mut w, &format!("{b}.{attn}.k_norm.weight"), &[hd]);
        }
        synth_tensor(&mut w, &format!("{b}.mlp.layer1.weight"), &[ff, h]);
        synth_tensor(&mut w, &format!("{b}.mlp.layer2.weight"), &[h, ff]);
    }
    synth_tensor(
        &mut w,
        "net.final_layer.adaln_modulation.1.weight",
        &[lora, h],
    );
    synth_tensor(
        &mut w,
        "net.final_layer.adaln_modulation.2.weight",
        &[2 * h, lora],
    );
    synth_tensor(&mut w, "net.final_layer.linear.weight", &[proj_out, h]);
    w
}

/// The deterministic synthetic inputs: a NON-SQUARE latent `[1,16,1,4,6]` (post-patch grid 2×3 — so an
/// h/w RoPE swap is detectable) + encoder `[1,5,8]` + `sigma=0.7`. Seeds match nothing licensed.
fn synth_inputs() -> (Array, Array, Array) {
    let latent = Array::from_slice(&synth_lcg(16 * 4 * 6, 1), &[1, 16, 1, 4, 6]);
    let encoder = Array::from_slice(&synth_lcg(5 * 8, 2), &[1, 5, 8]);
    let sigma = Array::from_slice(&[0.7f32], &[1]);
    (latent, encoder, sigma)
}

/// Run block0 + the full forward with the synthetic weights (fp32 on the Metal GPU).
fn synth_run() -> (Array, Array) {
    let cfg = synth_cfg();
    let w = synth_weights(&cfg);
    let dit = CosmosDiT::from_weights(&w, "net", cfg).expect("synthetic DiT loads");
    let (latent, encoder, sigma) = synth_inputs();
    let block0 = dit
        .forward_hidden(&latent, &sigma, &encoder, Dtype::Float32, Some(1))
        .expect("forward_hidden");
    let full = dit
        .forward(&latent, &sigma, &encoder, Dtype::Float32)
        .expect("forward");
    (block0, full)
}

/// mean/std/l2 + a strided sample set of a tensor, as a JSON summary (mirrors the Python `summarize`).
fn synth_summarize(arr: &Array, n_samples: usize) -> Value {
    let flat: Vec<f64> = arr
        .as_dtype(Dtype::Float32)
        .unwrap()
        .as_slice::<f32>()
        .iter()
        .map(|&x| x as f64)
        .collect();
    let total = flat.len();
    let mean = flat.iter().sum::<f64>() / total as f64;
    let std = (flat.iter().map(|&x| (x - mean).powi(2)).sum::<f64>() / total as f64).sqrt();
    let l2 = flat.iter().map(|&x| x * x).sum::<f64>().sqrt();
    let mut idx: Vec<usize> = if total <= n_samples {
        (0..total).collect()
    } else {
        (0..n_samples)
            .map(|i| (i as f64 * (total as f64 - 1.0) / (n_samples as f64 - 1.0)).round() as usize)
            .collect()
    };
    idx.dedup();
    let samples: Vec<f64> = idx.iter().map(|&i| flat[i]).collect();
    serde_json::json!({
        "shape": arr.shape().to_vec(),
        "count": total,
        "mean": mean,
        "std": std,
        "l2": l2,
        "sample_indices": idx,
        "sample_values": samples,
    })
}

/// Assert a synthetic output against its committed summary with a **distribution / relative-L2** gate —
/// the SAME shape the real-weights stages use (`parity_real_weights::assert_sampled_rel_l2`), and for the
/// same reason. A per-**element** `atol`/`rtol` gate is the WRONG shape here: cross-**device** Metal fp32
/// matmul reorders its reductions, so a single small-magnitude element of a RAW pre-norm block hidden can
/// differ by a few e-3 *relative* between the Mac that blessed this golden and the CI runner — the CI
/// failure that motivated this change was |Δ| 5.9e-4 on |want| ≈ 0.153 (≈3.9e-3 relative), squarely
/// inside this codebase's long-documented ~1e-3 Metal-matmul relative spread. The old per-element bound
/// (`atol 1e-4 + rtol 1e-3` ≈ 2.5e-4 there) sat *below* that spread and so was red on CI while green on
/// the blessing machine — a device-portability bug in the gate, not a numeric regression.
///
/// So we gate the DISTRIBUTION instead: aggregate stats (mean/std/l2 within `stat_rtol`) plus a sampled
/// relative-L2 `‖got−want‖₂ / ‖want‖₂` over the fixed index set (under `max_rel_l2`). rel-L2 is
/// norm-weighted — it is NOT dominated by one small-magnitude element's relative rounding (the thing that
/// varies device-to-device), yet a structural regression (dropped 17→16 concat, swapped adaLN
/// scale/shift, transposed h/w RoPE axis) still moves it by ORDERS of magnitude. Returns the sampled
/// rel-L2 so the caller can log it. NOTE: `stat_rtol`/`max_rel_l2` absorb cross-**device** Metal fp32
/// matmul variance — not merely single-machine run-to-run noise.
fn assert_synth(got: &Array, g: &Value, label: &str, stat_rtol: f64, max_rel_l2: f64) -> f64 {
    let want_shape: Vec<i32> = g["shape"]
        .as_array()
        .unwrap()
        .iter()
        .map(|x| x.as_i64().unwrap() as i32)
        .collect();
    assert_eq!(got.shape(), &want_shape[..], "{label}: shape");
    let flat: Vec<f64> = got
        .as_dtype(Dtype::Float32)
        .unwrap()
        .as_slice::<f32>()
        .iter()
        .map(|&x| x as f64)
        .collect();
    let count = g["count"].as_u64().unwrap() as usize;
    assert_eq!(flat.len(), count, "{label}: count");
    // (1) Aggregate distribution gate — cross-device fp32 reordering barely moves these sums; a
    //     structural bug shifts them by orders of magnitude.
    let mean = flat.iter().sum::<f64>() / count as f64;
    let std = (flat.iter().map(|&x| (x - mean).powi(2)).sum::<f64>() / count as f64).sqrt();
    let l2 = flat.iter().map(|&x| x * x).sum::<f64>().sqrt();
    let (gm, gs, gl) = (
        g["mean"].as_f64().unwrap(),
        g["std"].as_f64().unwrap(),
        g["l2"].as_f64().unwrap(),
    );
    let rel = |a: f64, b: f64| (a - b).abs() / b.abs().max(1e-6);
    assert!(
        (mean - gm).abs() < stat_rtol * gs.abs().max(1e-3),
        "{label}: mean drift {mean} vs {gm}"
    );
    assert!(rel(std, gs) < stat_rtol, "{label}: std drift {std} vs {gs}");
    assert!(rel(l2, gl) < stat_rtol, "{label}: l2 drift {l2} vs {gl}");
    // (2) Sampled relative-L2 gate — the primary structural check.
    let idx: Vec<usize> = g["sample_indices"]
        .as_array()
        .unwrap()
        .iter()
        .map(|x| x.as_u64().unwrap() as usize)
        .collect();
    let vals: Vec<f64> = g["sample_values"]
        .as_array()
        .unwrap()
        .iter()
        .map(|x| x.as_f64().unwrap())
        .collect();
    assert!(idx.len() >= 16, "{label}: too few samples ({})", idx.len());
    let mut num = 0f64;
    let mut den = 0f64;
    for (&i, &want) in idx.iter().zip(&vals) {
        let d = flat[i] - want;
        num += d * d;
        den += want * want;
    }
    let rel_l2 = (num / den.max(1e-12)).sqrt();
    println!(
        "[{label}] rel-L2 = {rel_l2:.4e} (bound {max_rel_l2:.1e}); stats mean {mean:.5}/{gm:.5} \
         std {std:.5}/{gs:.5} l2 {l2:.4}/{gl:.4}"
    );
    assert!(
        rel_l2 < max_rel_l2,
        "{label}: sampled rel-L2 {rel_l2:.4e} exceeds {max_rel_l2:.1e} — a structural regression, \
         not cross-device fp32 noise"
    );
    rel_l2
}

/// **CI numeric protection for the DiT forward** — runs the synthetic-weight Cosmos DiT on Metal (no
/// licensed weights, no snapshot) and asserts block0 + the full velocity against the committed golden.
#[test]
fn synthetic_dit_forward_golden() {
    let g = load_golden("dit_synthetic_golden.json");
    let (block0, full) = synth_run();
    // block0 is the RAW pre-norm block-0 hidden — the worst-conditioned tensor in the forward, and the one
    // whose small-magnitude elements diverge most device-to-device (the CI failure that motivated this gate
    // was a block0 element). Gate its distribution LOOSELY, as a corroborating backstop: `max_rel_l2 =
    // 1.2e-2` is ~3.1× above the worst-case cross-device spread (~3.9e-3 relative) and ~24× above the
    // realistic aggregate spread (~5e-4), while still tripping on every structural mutation (measured on
    // this fixture: drop-concat 1.30, adaLN-swap 0.170, RoPE-h/w-swap 0.038 — the last only ~3.2× over this
    // bound because a 2×3 grid barely reshuffles under an axis swap; the FINAL-velocity gate below is what
    // carries the >10× guarantee for it).
    assert_synth(&block0, &g["block0"], "synth_block0", 5e-3, 1.2e-2);
    // The final velocity is norm_out + proj_out (LayerNorm-conditioned → the small-magnitude blow-ups that
    // make block0 noisy are gone), so it is the TIGHT gate. `max_rel_l2 = 5e-3` clears the (larger,
    // accumulated) cross-device spread with ~6× headroom yet catches ALL three mutations with margin —
    // measured: drop-concat 1.29 (258×), adaLN-swap 0.313 (63×), RoPE-h/w-swap 0.065 (13×, the smallest).
    assert_synth(&full, &g["full"], "synth_full", 3e-3, 5e-3);
}

