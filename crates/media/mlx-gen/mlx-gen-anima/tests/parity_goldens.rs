//! Anima MLX-port parity goldens (sc-10524, epic 10512) — the **CI-runnable** stages: they read the
//! committed golden JSON (no Python at test time) and need **no** licensed weights, so they run in the
//! macOS `cargo test --workspace` lane.
//!
//! Covered here:
//!   * **Stage 1** — Qwen2 + T5 tokenizers → exact token ids for the fixture prompts.
//!   * **Stage 5** — `linspace(1, 1/N, N)` sigma schedule + static `shift=3.0` → sigmas/timesteps.
//!   * **Stage 6** — assert the VAE is REUSED (`mlx_gen_qwen_image::QwenVae`), not forked.
//!   * **DiT forward (synthetic)** — a small deterministic-random-weight Cosmos DiT run on the CI Metal
//!     GPU (NO licensed weights, NO snapshot), pinned to a committed golden. This is the CI numeric
//!     protection for the transformer forward (the real-weights DiT stages are `#[ignore]`d).
//!   * **Trap** — 17-channel patch-embed input (`concat_padding_mask`).
//!   * **Trap** — per-axis RoPE OOD reject (never index OOB).
//!   * **Trap** — prompt weighting is **not yet implemented** (documented finding → follow-up story).
//!
//! The real-weights stages (2 Qwen3, 3 conditioner, 4 DiT, 7 e2e) live in `tests/parity_real_weights.rs`
//! and `tests/real_weights.rs`, all `#[ignore]`d + weights-gated (they never run in CI).
//!
//! Goldens regenerated by `tests/fixtures/gen_anima_parity_goldens.py` (diffusers 0.39.0, Apache-2.0).

