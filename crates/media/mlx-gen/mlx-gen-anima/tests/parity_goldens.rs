// CI DiT-forward golden — SYNTHETIC deterministic-random weights (sc-10524 review). Closes the hole
// the review flagged: the real-weights DiT stages (2/3/4/7) are `#[ignore]`d + snapshot-gated, so a
// numeric regression in the transformer forward shipped GREEN — CI had ZERO numeric protection for it.
//
// This test runs a SMALL Cosmos DiT (2 layers, hidden 12, NON-SQUARE 4×6 latent) with weights from a
// fixed-seed deterministic RNG — NO licensed weights, NO HF snapshot — on the CI Metal GPU, and pins
// committed numbers. It catches structural regressions with no weights: the 17-vs-16 padding-mask
// concat, adaLN modulation wiring, the h/w RoPE axis (the grid is non-square, so a transposition
// changes the output), a wrong checkpoint prefix, and any shape drift.
//
// The pinned numbers are the reviewer-verified-correct Rust forward frozen as a REGRESSION LOCK (the
// correctness ORACLE is the real-weights stages, which the reviewer independently reproduced). Its
// sensitivity — that it is NOT a vacuous self-check — is proven by the PR's mutation checks: breaking
// the channel concat and the adaLN modulation each make this test fail.
//
// Regenerate the golden (only when the forward legitimately changes) with:
//   ANIMA_BLESS_SYNTH=1 cargo test -p mlx-gen-anima --test parity_goldens -- \
//       --ignored bless_synthetic_dit_golden --nocapture
